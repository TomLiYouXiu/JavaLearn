package com.base;

//数据类型
public class Demo01 {
    public static void main(String[] args) {
        //八大基本数据类型

        //整数
        int num1=10; //最常用
        byte num2=20;
        short num3=20;
        long num4=20L; //为了区分long一般加上L

        //小数 ：浮点数
        float num5=40.5F; //为了区分float一般再加上F
        double num6=3.1415926;

        //字符
        char name ='A';
        //字符串，String不是关键字，是一个类
        String nameA="李文龙";

        //布尔值
        boolean flag=false;
        boolean flag1=true;
    }
}
